#Operadores lógicos

#Operador AND (significa "e")
Idade = 20

if Idade >= 18 and Idade <= 60: 
    print("Infelizmente você já é um adulto :(")
#Se as duas condições forem verdadeiras ocorre a execução

#Operador OU (significa "ou")
GanhouPremio = 1

if GanhouPremio == 1 or GanhouPremio == 2:
    print("Parabéns! Você ganhou :)")
#Se uma das condições forem verdadeiras ocorre a execução

#Operador NOT (significa "negação/contrário")
Questionamento = False

while not Questionamento:
    print("Questionamento é verdadeiro")
    Questionamento = True



